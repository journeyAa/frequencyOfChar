package day07_Unary_ShortHand;

public class Post_vs_Pre {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(++a );
        System.out.println(a);

        System.out.println("=================================================");

        int b = 10;
        System.out.println(b++);
        int c = b + 1;
        System.out.println(c);

        System.out.println("+++++++++++++++++++++++++++++++++++++++++++++++++++");


        int x = 8;
        System.out.println(x--);

        int y = 8;
        System.out.println(--y);





    }



}
