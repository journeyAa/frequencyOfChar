package LoopPractice;

public class forLoopPractice {
    public static void main(String[] args) {

        for (int i = 100; i >= 1 ; i--) {
            System.out.print(i + " ");

        }
        System.out.println();
        System.out.println("===============================================================");

        for (int a = 1; a <= 100 ; a += 2) {
            System.out.print(a + " ");
        }
        System.out.println();
        System.out.println("==============================================================");

        for (int b = 0; b <= 100; b += 2) {
            System.out.print(b + " ");

        }

        System.out.println();
        System.out.println("==================================================================");

        for (char ch1 = 'a'; ch1 <= 'z' ; ch1++) {
            System.out.print(ch1 + " ");
        }
        System.out.println();
        System.out.println("==================================================================");

        for(char ch2 = 'A'; ch2 <= 'Z'; ch2++){
            System.out.print(ch2 + " ");
        }













    }
}
